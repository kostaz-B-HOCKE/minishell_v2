#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*ll;

	ll = (t_list *)malloc(sizeof(*ll));
	if (!ll)
		return (NULL);
	ll ->content = content;
	ll ->next = NULL;
	return (ll);
}
